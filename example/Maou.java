package example;

public class Maou {
    private int hp;
    private int b;
    private int MAX_HP;

    public void kaihuku() {
        this.hp = this.hp + b;
    }

    public void setHp(int hp) {
        this.hp = hp;
    }

    public void setMaxHp(int maxHp) {
        this.MAX_HP = maxHp;
    }

    public void setB(int b) {
        this.b = b;
    }

    public int getHp() {
        return this.hp;
    }

    public int getB() {
        return this.b;
    }

    public int getMaxHP() {
        return this.MAX_HP;
    }

}
